// ── Helper function ────────────────────────────────────────────────────────────
export const t = (path, lang = 'en') => {
  const keys = path.split('.');
  let val = TRANSLATIONS[lang] || TRANSLATIONS.en;
  for (const k of keys) {
    val = val?.[k];
    if (val === undefined) {
      // Fallback to English
      let fallback = TRANSLATIONS.en;
      for (const fk of keys) { fallback = fallback?.[fk]; }
      return fallback ?? path;
    }
  }
  return val;
};

export default TRANSLATIONS;
